package firststeps;

public class CharAndBoolean {
    public static void main(String[] args) {



        char myChar = 'D';
        System.out.println(myChar);

        char myUnicode = '\u0044';
        System.out.println(myUnicode);

        char myDecimalCode = 68;
        System.out.println(myDecimalCode);

        System.out.println("\n");

        char myQuestionMark =  '?';
        System.out.println(myQuestionMark);

        char myQuestionMarkUnicode = '\u003f';
        System.out.println(myQuestionMarkUnicode);

        char myQuestionmarkDecimalCode = 63;
        System.out.println(myQuestionmarkDecimalCode);
        System.out.println("\n");
        System.out.println("My values are " + myQuestionMark + myQuestionMarkUnicode + myQuestionmarkDecimalCode);



    }
}
