package firststeps;

public class AbbreviatingOperators {

    public static void main(String[] args) {

        int result = 1;
        result++;

        System.out.println("result = " + result);

        System.out.println("\n");

        int result2 = 10;

        result2--;

        System.out.println("My result is = " + result2);

        int result3 = 10;
        result3 -= 4;
        System.out.println("My result is = " + result3);





    }
}

